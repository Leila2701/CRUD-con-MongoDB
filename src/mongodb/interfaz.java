    public void listar() throws UnknownHostException{
        tabla= db.getCollection("cliente");
        cursor = tabla.find();
        
        String[] columnNames={"DNI","NOMBRE","APELLIDO","CELULAR","DIRECCION"};
        DefaultTableModel model = new DefaultTableModel(columnNames,0);
        
        while (cursor.hasNext()){
            DBObject obj = cursor.next();
            String dni = (String) obj.get("dni");
            String nombre = (String) obj.get("nombre");
            String apellido = (String) obj.get("apellido");
            String telefono = (String) obj.get("celular");
            String direccion = (String) obj.get("direccion");
            
            model.addRow(new Object[] { dni , nombre, apellido, telefono, direccion});
        }
        Tabla.setModel(model);
    }
    
    public void buscar() throws UnknownHostException{
        
        documento.put("dni",txtDNI.getText());
        cursor = tabla.find(documento);
        
        while(cursor.hasNext()){
            DBObject obj = cursor.next();
            String nombre = (String) obj.get("nombre");
            String apellido = (String) obj.get("apellido");
            String telefono = (String) obj.get("celular");
            String direccion = (String) obj.get("direccion");
            
            txtNombre.setText(nombre);
            txtApellido.setText(apellido);
            txtTelefono.setText(telefono);
            txtDireccion.setText(direccion);

        }
    }
    
