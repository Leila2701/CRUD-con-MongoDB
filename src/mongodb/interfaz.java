    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (txtDNI.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Ingrese un DNI");
        }
        else{
            documento.put("dni", txtDNI.getText());
            tabla.remove(documento);
            txtDNI.setText("");
            txtNombre.setText("");
            txtApellido.setText("");
            txtTelefono.setText("");
            txtDireccion.setText("");
            txtDNI.requestFocus();
            try {
                listar();
            } catch (UnknownHostException ex) {
                Logger.getLogger(interfaz.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if ( txtDNI.getText().equals("") || txtNombre.getText().equals("") || txtApellido.getText().equals("") || txtTelefono.getText().equals("") || txtDireccion.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Es obligatorio llenar todos los campos");
        }
        else{
            BasicDBObject document = new BasicDBObject();
            document.put("dni", txtDNI.getText());
            document.put("nombre", txtNombre.getText());
            document.put("apellido", txtApellido.getText());
            document.put("celular", txtTelefono.getText());
            document.put("direccion", txtDireccion.getText());
            tabla.insert(document);
            txtDNI.setText("");
            txtNombre.setText("");
            txtApellido.setText("");
            txtTelefono.setText("");
            txtDireccion.setText("");
            txtDNI.requestFocus();
            try {
                listar();
            } catch (UnknownHostException ex) {
                Logger.getLogger(interfaz.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if ( txtDNI.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Ingrese el DNI correcto");
        }
        else{
            documento.put("dni", txtDNI.getText());
            
            BasicDBObject documento_nuevo = new BasicDBObject();
            
            documento_nuevo.put("dni", txtDNI.getText());
            documento_nuevo.put("nombre", txtNombre.getText());
            documento_nuevo.put("apellido", txtApellido.getText());
            documento_nuevo.put("celular", txtTelefono.getText());
            documento_nuevo.put("direccion", txtDireccion.getText());
            tabla.findAndModify(documento, documento_nuevo);
            
            try {
                listar();
            } catch (UnknownHostException ex) {
                Logger.getLogger(interfaz.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnEditarActionPerformed

